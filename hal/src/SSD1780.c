uint8_t data_buf[1026];

bool isInverted = false;

uint8_t SSD1780_init(uint8_t i2c_dev)
{
    uint8_t rc;
    rc = _i2c_init(i2c_dev, SSD1780_I2C_ADDR);
    if (rc > 0)
        return rc;
        
    // test i2c connection
    uint8_t cmd = SSD1780_COMM_CONTROL_BYTE;
    uint8_t result = 0;
    _i2c_write(&cmd, 1);
    _i2c_read(&result, 1);
    if (result == 0)
        return 1;
    
    return 0;
}

uint8_t SSD1780_defaultConfig()
{
    uint16_t i = 0;
    data_buf[i++] = SSD1780_COMM_CONTROL_BYTE;  //command control byte
    data_buf[i++] = SSD1780_COMM_DISPLAY_OFF;   //display off
    data_buf[i++] = SSD1780_COMM_SET_COL_ADDR;
    data_buf[i++] = 0x00;
    data_buf[i++] = 0x7F;
    data_buf[i++] = SSD1780_COMM_SET_PAGE_ADDR;
    data_buf[i++] = 0x00;
    data_buf[i++] = 0x07;
    data_buf[i++] = SSD1780_COMM_CLK_SET;       //SETDISPLAYCLOCKDIV
    data_buf[i++] = 0x80;                       // the suggested ratio 0x80
    data_buf[i++] = SSD1780_COMM_MULTIPLEX;     //SSD1780_SETMULTIPLEX
    data_buf[i++] = 31;             // height is 32 or 64 (always -1)
    data_buf[i++] = SSD1780_COMM_VERT_OFFSET;   //SETDISPLAYOFFSET
    data_buf[i++] = 0;                          //no offset
    data_buf[i++] = SSD1780_COMM_START_LINE | 0b000100000;    //SETSTARTLINE
    data_buf[i++] = SSD1780_COMM_CHARGE_PUMP;   //CHARGEPUMP
    data_buf[i++] = 0x14;                       //turn on charge pump
    data_buf[i++] = SSD1780_COMM_MEMORY_MODE;   //MEMORYMODE
    data_buf[i++] = SSD1780_VERT_MODE;          // page mode
    data_buf[i++] = SSD1780_COMM_HORIZ_NORM | 0;    //SEGREMAP  Mirror screen horizontally (A0)
    data_buf[i++] = SSD1780_COMM_SCAN_NORM;     //COMSCANDEC Rotate screen vertically (C0)
    data_buf[i++] = SSD1780_COMM_COM_PIN;       //HARDWARE PIN 
    data_buf[i++] = 0x02;                       // for 32 lines
    data_buf[i++] = SSD1780_COMM_CONTRAST;      //SETCONTRAST
    data_buf[i++] = 0x7f;                       // default contract value
    data_buf[i++] = SSD1780_COMM_PRECHARGE;     //SETPRECHARGE
    data_buf[i++] = 0xf1;                       // default precharge value
    data_buf[i++] = SSD1780_COMM_DESELECT_LV;   //SETVCOMDETECT                
    data_buf[i++] = 0x40;                       // default deselect value
    data_buf[i++] = SSD1780_COMM_RESUME_RAM;    //DISPLAYALLON_RESUME
    data_buf[i++] = SSD1780_COMM_DISP_NORM;     //NORMALDISPLAY
    data_buf[i++] = SSD1780_COMM_DISPLAY_ON;    //DISPLAY ON             
    data_buf[i++] = SSD1780_COMM_DISABLE_SCROLL;//Stop scroll
    
    return _i2c_write(data_buf, i);
}

uint8_t SSD1780_close() {
    return _i2c_close();
}

uint8_t SSD1780_clear() {
    // uint8_t* buffer = malloc(sizeof(uint8_t)*1025);
    memset(data_buf, 0x00, 1026);
    data_buf[0] = SSD1780_DATA_CONTROL_BYTE;
    _i2c_write(data_buf, 1026);
    return 0;
}

uint8_t SSD1780_invertDisplay() {
    uint16_t i = 0;
    data_buf[i++] = SSD1780_COMM_CONTROL_BYTE;
    if (isInverted) {
        data_buf[i++] = SSD1780_COMM_DISP_NORM;
        isInverted = false;
    } else {
        data_buf[i++] = SSD1780_COMM_DISP_INVERSE;
        isInverted = true;
    }
    return _i2c_write(data_buf, i);
}
// Prints message to the frame buffer on specified line (0-3)
// Fits up to 16 char
void SSD1780_print2Buffer(uint8_t line, char* message) {
    int max = strlen(message);
    if (max > 16) {
        max = 16;
    }
    for (int charColumn = 0; charColumn < max; charColumn++) {
        for (int i = 0; i < 8; i++) { 
            if ((8*i+8)+line+(64*charColumn) > 1024) {
                break;
            }
            frame_buffer[(8*i+8)+line+(64*charColumn)] = font8x8[8*(message[charColumn]-32)+i];
        }  
    }
}

// Prints message to the frame buffer on specified line (0-1)
// Fits up to 8 char
void SSD1780_print2BufferLarge(uint8_t line, char* message) {
    int max = strlen(message);
    int xSkew = 8;
    int ySkew = 0;            
    uint8_t part1 = 0;
    uint8_t part2 = 0;
    if (max > 16) {
        max = 16;
    }
    for (int charColumn = 0; charColumn < max; charColumn++) {
        for (int i = 0; i < 8; i++) { 
            uint8_t pixel = font8x8[8*(message[charColumn]-32)+i];

            int part = 0;
            // yskew char
            for (int j = 0; j < 8; j++) {
                // part |= (((uint16_t)pixel & (0b10000000 >> j)) >> j);
                // part |= (((uint16_t)pixel & (0b10000000 >> j)) >> (j+1));
                part |= ((((uint16_t)pixel << 8) & (0b1000000000000000 >> j)) >> j);
                part |= ((((uint16_t)pixel << 8) & (0b1000000000000000 >> j)) >> (j+1));
            }
            part1 = (uint8_t)(part);
            part2 = (uint8_t)(part >> 8);
            // printf("part 1: %i\n", part1);
            // printf("part 2: %i\n\n", part2);

            if ((8*xSkew+8)+line+(64*charColumn+ySkew) > 1024) {
                break;
            }
            frame_buffer[(8*xSkew+8)+line+(64*charColumn)] = part2;
            frame_buffer[(8*xSkew+8)+1+line+(64*charColumn)] = part1;
            xSkew++;
            if ((8*xSkew+8)+line+(64*charColumn+ySkew) > 1024) {
                break;
            }
            frame_buffer[(8*xSkew+8)+line+(64*charColumn)] = part2;
            frame_buffer[(8*xSkew+8)+1+line+(64*charColumn)] = part1;
            xSkew++;
        }  
        xSkew -= 8; // fixes spacing
    }
}

void SSD1780_putPixel2Buffer(uint8_t x, uint8_t y) {
    uint8_t index = 7 - y%8;
    u_int8_t pixel = 0b1 << index;
    frame_buffer[(x*8)+((y)/8)] = pixel;
}

